void boost_Init(){
	/* setup PWM on OC0B (PA7) */
	/* initially set pin high -> FET is off */
	PORTA |= (1<<PA7);
	DDRA |= (1<<PA7);

	/* initialize default voltage/current limits */
	boost_setCurrent(0);
	boost_setMaxVoltage(41000);
}

void boost_Enable(){
	/* initialize PWM value, start with lowest PWM value */
	boost.PWM = 0;
	boost_updatePWM();
	/* initialize OCR0A/OCR0B (would normally be done in ISR) */
	OCR0A = top;
	OCR0B = compare;
	/* set inverting fast PWM mode, using OCR0A as TOP */
	TCCR0A |= (1<<COM0B0) | (1<<COM0B1) | (1<<WGM01) | (1<<WGM00);
	/* set PWM as fast as possible, results in 78kHz PWM frequency */
	TCCR0B |= (1<<CS00) | (1<<WGM02);
	/* set callback to update boost converter */
	adc_setCallback(boost_Update);
}

void boost_Disable(){
	/* set pin high -> FET is off (should already be high, just a safety measure) */
	PORTA |= (1<<PA7);
	DDRA |= (1<<PA7);
	/* disable PWM */
	TCCR0A = TCCR0B = 0;
	/*disable ADC callback */
	adc_setCallback(NULL);
}

void boost_Update(void){
	if (adc.raw[ADC_VOLTAGE] > boost.setVoltageRaw
			|| adc.raw[ADC_CURRENT] > boost.setCurrentRaw) {
		/* current or voltage at limit -> decrease duty cycle */
		if (boost.PWM > 0)
			boost.PWM--;
	} else {
		/* not at limit -> increase duty cycle */
		if (boost.PWM < sizeof(PWM_LOOKUP)/2 - 1)
			boost.PWM++;
	}
	boost_updatePWM();
	/* activate value set ISR */
	TIMSK0 = (1<<TOIE0);
}

void boost_setCurrent(uint16_t mA){
	if(mA > BOOST_MAX_CURRENT)
		/* limit current to maximum */
		mA = BOOST_MAX_CURRENT;
	/* calculate raw ADC value for given current */
	uint16_t adcVoltage = ((uint32_t) mA * BOOST_SHUNT_VALUE) / 1000;
	/* convert from voltage in mV to raw ADC (10bit, 1V1 reference) */
	boost.setCurrentRaw = ((uint32_t) adcVoltage * 93) / 100;
}

void boost_setMaxVoltage(uint16_t mV){
	if(mV > BOOST_MAX_VOLTAGE)
		/* limit voltage to maximum */
		mV = BOOST_MAX_VOLTAGE;
	/* calculate raw ADC value for given voltage */
	uint16_t adcVoltage = ((uint32_t) mV * 1000) / BOOST_VOL_SENS_FACT;
	/* convert from voltage in mV to raw ADC (10bit, 1V1 reference) */
	boost.setVoltageRaw = ((uint32_t) adcVoltage * 93) / 100;
}

void boost_updatePWM(void){
	/* extract PWM generation values and store in registers */
	compare = pgm_read_byte(&PWM_LOOKUP[boost.PWM][0]);
	top = pgm_read_byte(&PWM_LOOKUP[boost.PWM][1]);
}

ISR(TIM0_OVF_vect){
	/* WARNING: naked ISR */
	/* DO NOT CHANGE ANY REGISTERS IN HERE (TAKE CARE OF SREG, TOO) */
	/* just copy the compare and top values into timer config registers */
	OCR0B = compare;
	OCR0A = top;
	/* deactivate this ISR (will be activated if values change) */
	TIMSK0 = 0;
	/* return to main program */
	asm volatile("reti");
	/* necessary to keep compiler from generating return value */
	__builtin_unreachable();
}
